# Cálculo do maior e do menor número
n1 = int(input('Informe o primeiro número inteiro: '))
n2 = int(input('Informe o segundo número inteiro: '))
if (n1 > n2):
    print('\nO PRIMEIRO número é maior')
elif (n1 < n2):
    print('\nO SEGUNDO número é maior')
else:
    print('\nO dois números são iguais!!!')


